def make_example(user_msg, assistant_msg):
    return {
        "messages": [
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": user_msg},
            {"role": "assistant", "content": assistant_msg},
        ]
    }


def generate_multi_turn(categories, n=300):
    random.seed(55)
    multi = []
    followups_user = [
        "طيب شكراً", "تمام إن شاء الله", "جزاك الله خير",
        "أوكي بنجرب", "وإذا ما نفع؟", "عندي سؤال ثاني",
    ]
    followups_assistant = [
        "العفو! إذا تحتاجون شي ثاني أنا حاضر.",
        "إن شاء الله يكون خير. جربوا وإذا تحتاجون شي ثاني ارجعوا اسألوا.",
        "بالتوفيق! والصبر مفتاح كل شي. أنا هني إذا تحتاجون.",
        "ابشروا! جربوا وشوفوا. وإذا ما نفع عندنا أفكار ثانية.",
        "تفضل اسأل! أنا حاضر أساعدكم.",
        "على راسي! شو السؤال؟",
    ]
    for _ in range(n):
        cat = random.choice(categories)
        t_u, t_a = random.choice(cat)
        f_u = random.choice(followups_user)
        f_a = random.choice(followups_assistant)
        multi.append({
            "messages": [
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": t_u},
                {"role": "assistant", "content": t_a},
                {"role": "user", "content": f_u},
                {"role": "assistant", "content": f_a},
            ]
        })
    return multi


def main():
    output_path = Path("data/raw/conversations_weakness_fixes.jsonl")

    all_examples = []
    all_cats = [DIALECT_FIX, PERSONA_FIX, REFUSALS_FIRM, CONCISE_WARM]

    for cat in all_cats:
        for u, a in cat:
            all_examples.append(make_example(u, a))

    single = len(all_examples)
    print(f"Single-turn fixes: {single}")

    multi = generate_multi_turn(all_cats, n=400)
    all_examples.extend(multi)
    print(f"Multi-turn: {len(multi)}")

    random.seed(88)
    random.shuffle(all_examples)

    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as f:
        for ex in all_examples:
            f.write(json.dumps(ex, ensure_ascii=False) + "\n")

    print(f"\nTotal weakness fixes: {len(all_examples)} en {output_path}")


if __name__ == "__main__":
    main()
